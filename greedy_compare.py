import pickle


with open("points.pickle",'rb') as pf:
    data0 = pickle.load(pf)

with open("random_order.pickle",'rb') as pf:
    data1 = pickle.load(pf)

same = 0
diff = 0
same_length = 0
diff_length = 0

for l in data1.keys():
    points0 = sorted(data0[l])
    points1 = sorted(data1[l])
    for i in range(min(len(points0),len(points1))):
        if points0[i] == points1[i]:
            same += 1
        else:
            diff += 1
    if len(points0) <= len(points1):
        same_length += 1
    else:
        diff_length += 1

print(same, diff)
print(same_length, diff_length)


